class Solution {
public:
    TreeNode *pre = nullptr;
    void flatten(TreeNode* root) {
        if(root == nullptr) return;
        flatten(root->right);
        flatten(root->left);
        root->left = nullptr;
        root->right = pre;
        pre = root;
    }
};
