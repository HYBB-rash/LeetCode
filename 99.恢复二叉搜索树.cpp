//     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
// };
class Solution {
public:
    vector<int> ans;
    int cnt = 0;
    void dfs(TreeNode* root){
        if(root == NULL) return;
        dfs(root->left);
        ans.push_back(root->val);
        dfs(root->right);
    }
    void rec(TreeNode* root){
        if(root == NULL) return;
        rec(root->left);
        root->val = ans[cnt++];
        rec(root->right);
    }
    void recoverTree(TreeNode* root) {
        dfs(root);
        sort(ans.begin(), ans.end());
        rec(root);
    }
};
